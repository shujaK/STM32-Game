// sprite: packed pix2 data
// w_packed: width in pixels rounded up to even (for packing), must be even
// w, h: true sprite dimensions
// x, y are the centre pixel of the sprite
static void
draw_sprite (frame *f, int x, int y, const uint8_t *sprite,
	     uint16_t w_packed, uint16_t w, uint16_t h)
{
  if (!f || !sprite)
    return;
  if (w == 0 || h == 0)
    return;
  if ((w_packed & 1) != 0)
    return; // packed width must be even for nibble pairs
  if (w_packed < w)
    return; // packed width must cover true width

  uint16_t bytes_per_row = w_packed / 2;

  int ox = x - (int) ((w - 1) / 2);
  int oy = y - (int) ((h - 1) / 2);

  int x0 = (ox < 0) ? 0 : ox;
  int y0 = (oy < 0) ? 0 : oy;
  int x1 = ox + (int) w;
  int y1 = oy + (int) h;

  if (x0 >= IMG_COL || y0 >= IMG_ROW || x1 <= 0 || y1 <= 0)
    return; // entirely off-screen

  if (x1 > IMG_COL)
    x1 = IMG_COL;
  if (y1 > IMG_ROW)
    y1 = IMG_ROW;

  for (int dst_y = y0; dst_y < y1; ++dst_y)
    {
      int src_row = dst_y - oy;
      const uint8_t *row_ptr = sprite + src_row * bytes_per_row;

      for (int dst_x = x0; dst_x < x1; ++dst_x)
	{
	  int src_col = dst_x - ox;
	  if (src_col >= w)
	    continue;

	  uint8_t packed = row_ptr[src_col >> 1];
	  uint8_t pix = (src_col & 1) ? (packed & 0x0F) : (packed >> 4);
	  if (pix != 0x0)
	    {
	      write_pixel (f, (uint16_t) dst_x, (uint16_t) dst_y, pix);
	    }
	}
    }
}

